// ── Validation (used by both client + server test) ────────────────────────────

// Allowed fields per event type. Any key not in these lists is forbidden.
const BASE_KEYS = ["event", "schemaVersion", "weekKey", "session_id"];

const ALLOWED_KEYS: Record<string, string[]> = {
  auto_detect_started: [...BASE_KEYS, "fsaSupported"],
  auto_detect_completed: [...BASE_KEYS, "durationBucket", "result"],
  auto_detect_failed: [...BASE_KEYS, "durationBucket", "failureCode", "failureStage"],
  survey_responded: [...BASE_KEYS, "hardestStep", "setupTimeBucket"],
};

// Fields that must NEVER appear in any telemetry payload (privacy invariant).
// This regex matches any key that could carry raw content, paths, or secrets —
// including bearer/identity secrets (claimToken, token, bearer, authorization,
// nonce, apiKey, secret, credential): the claim token is body-only and banned
// from telemetry/analytics/error-capture (spec §2.10). None of these collide
// with an allowed key, and the forbidden check runs before the whitelist check.
const FORBIDDEN_KEY_RE =
  /^(content|message|prompt|response|completion|input|output|code|payload|payloadText|requestBody|rawBody|json|jsonl|rawLine|jsonlLine|line|firstLine|lastLine|path|folderPath|rootPath|cwd|projectName|repoName|fileName|directory|selectedDirectory|sample|preview|snippet|errorContext|offendingRecord|record|entry|error|exception|stack|cause|salt|deviceSalt|projectHashInput|projectSlug|hashPreimage|claimToken|claim|token|bearer|authorization|auth|secret|nonce|apiKey|apikey|credential|credentials|metadata|properties|context|extra|debug|userAgent|sessionId)$/i;

const DURATION_BUCKETS: DurationBucket[] = ["0-1m", "1-3m", "3-5m", "5-10m", "10-20m", "20m+"];
const DETECT_RESULTS: AutoDetectResult[] = ["summary_generated", "upload_accepted"];
const FAILURE_CODES: FailureCode[] = [
  "fsa_unavailable", "folder_rejected", "permission_denied",
  "no_supported_logs", "parse_failed", "summary_validation_failed",
  "upload_failed", "unknown",
];
const FAILURE_STAGES: FailureStage[] = [
  "capability_check", "folder_picker", "scan", "parse", "validate", "upload",
];
const HARDEST_STEPS: HardestStep[] = [
  "terminal_setup", "folder_selection", "browser_permission",
  "upload", "understanding_results", "other_predefined",
];

// session_id regex: 32 lowercase hex chars (128-bit random)
const SESSION_ID_RE = /^[a-f0-9]{32}$/;
// weekKey: YYYY-MM-DD
const WEEK_KEY_RE = /^\d{4}-\d{2}-\d{2}$/;

export type ValidationResult =
  | { ok: true; event: TelemetryEvent }
  | { ok: false; error: string };

function isPlainObject(v: unknown): v is Record<string, unknown> {
  return typeof v === "object" && v !== null && !Array.isArray(v);
}

// Validates a raw (potentially untrusted) payload against the telemetry schema.
// Returns ok:true + the typed event, or ok:false + an error string.
// This is the single source of truth for both client-side and server-side checks.
export function validateTelemetryEvent(raw: unknown): ValidationResult {
  if (!isPlainObject(raw)) return { ok: false, error: "Event must be a JSON object." };

  // Check for forbidden keys first (privacy gate)
  for (const k of Object.keys(raw)) {
    if (FORBIDDEN_KEY_RE.test(k)) {
      return { ok: false, error: `Forbidden field: "${k}"` };
    }
  }

  const eventType = raw["event"];
  if (typeof eventType !== "string" || !(eventType in ALLOWED_KEYS)) {
    return { ok: false, error: `Unknown event type: "${eventType}"` };
  }

  // Enforce no extra keys beyond the allowed list for this event type
  const allowed = ALLOWED_KEYS[eventType];
  for (const k of Object.keys(raw)) {
    if (!allowed.includes(k)) {
      return { ok: false, error: `Unexpected field "${k}" for event "${eventType}"` };
    }
  }

  if (raw["schemaVersion"] !== 1) {
    return { ok: false, error: "schemaVersion must be 1" };
  }
  if (typeof raw["weekKey"] !== "string" || !WEEK_KEY_RE.test(raw["weekKey"] as string)) {
    return { ok: false, error: "weekKey must be YYYY-MM-DD" };
  }
  if (typeof raw["session_id"] !== "string" || !SESSION_ID_RE.test(raw["session_id"] as string)) {
    return { ok: false, error: "session_id must be 32 lowercase hex chars" };
  }

  switch (eventType) {
    case "auto_detect_started":
      if (typeof raw["fsaSupported"] !== "boolean") {
        return { ok: false, error: "auto_detect_started.fsaSupported must be boolean" };
      }
      break;
    case "auto_detect_completed":
      if (!DURATION_BUCKETS.includes(raw["durationBucket"] as DurationBucket)) {
        return { ok: false, error: "auto_detect_completed.durationBucket is invalid" };
      }
      if (!DETECT_RESULTS.includes(raw["result"] as AutoDetectResult)) {
        return { ok: false, error: "auto_detect_completed.result is invalid" };
      }
      break;
    case "auto_detect_failed":
      if (!DURATION_BUCKETS.includes(raw["durationBucket"] as DurationBucket)) {
        return { ok: false, error: "auto_detect_failed.durationBucket is invalid" };
      }
      if (!FAILURE_CODES.includes(raw["failureCode"] as FailureCode)) {
        return { ok: false, error: "auto_detect_failed.failureCode is invalid" };
      }
      if (!FAILURE_STAGES.includes(raw["failureStage"] as FailureStage)) {
        return { ok: false, error: "auto_detect_failed.failureStage is invalid" };
      }
      break;
    case "survey_responded":
      if (!HARDEST_STEPS.includes(raw["hardestStep"] as HardestStep)) {
        return { ok: false, error: "survey_responded.hardestStep is invalid" };
      }
      if (!DURATION_BUCKETS.includes(raw["setupTimeBucket"] as DurationBucket)) {
        return { ok: false, error: "survey_responded.setupTimeBucket is invalid" };
      }
      break;
  }

  return { ok: true, event: raw as unknown as TelemetryEvent };
}

// ── Client send helper ────────────────────────────────────────────────────────

// Sends a telemetry event to the server. Fire-and-forget: errors are silently
// swallowed so that a telemetry failure never disrupts the main flow.
//
// sendBeacon is intentionally NOT used here: sendBeacon cannot attach custom
// Authorization headers, which are required for the HMAC collector token.
// fetch with keepalive: true provides equivalent unload-survival semantics.
export function sendTelemetryEvent(event: TelemetryEvent): void {
  // Validate locally before sending (defence in depth).
  const result = validateTelemetryEvent(event);
  if (!result.ok) return; // drop silently

  void (async () => {
    try {
      const body = JSON.stringify(event);
      // Fetch a single-use token bound to this telemetry POST.
      const tokenRes = await fetch("/api/internal/issue-collector-token", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ kind: "telemetry" }),
      });
      if (!tokenRes.ok) return; // drop silently if token issuance fails
      const tokenData = (await tokenRes.json()) as { token?: string };
      if (!tokenData.token) return;

      fetch("/api/telemetry/auto-detect", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "Authorization": `Bearer ${tokenData.token}`,
        },
        body,
        keepalive: true,
      }).catch(() => {});
    } catch {
      // Never throw from telemetry
    }
  })();
}

// ── Duration timer ────────────────────────────────────────────────────────────

// Returns a function that, when called, returns the elapsed DurationBucket.
export function startDurationTimer(): () => DurationBucket {
  const start = Date.now();
  return (): DurationBucket => {
    const ms = Date.now() - start;
    const min = ms / 60_000;
    if (min < 1) return "0-1m";
    if (min < 3) return "1-3m";
    if (min < 5) return "3-5m";
    if (min < 10) return "5-10m";
    if (min < 20) return "10-20m";
    return "20m+";
  };
}
