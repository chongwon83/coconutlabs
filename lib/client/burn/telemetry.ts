// lib/client/burn/telemetry.ts — typed telemetry event constructors (Axes 2–3).
//
// PRIVACY INVARIANT: every event type has an explicit whitelist of allowed fields
// (additionalProperties: false). Forbidden categories include: raw content,
// file paths, error messages, stack traces, salts, model/session internals, and
// bearer/identity secrets — the per-handle CLAIM token (lib/client/burn/
// claimToken.ts), the collector nonce, and any API key. The claim token is a
// bearer secret for identity (spec §2.10): it must NEVER reach an analytics or
// error-capture payload. The typed constructors below are the only event source
// and none accept a token, but FORBIDDEN_KEY_RE names them too as defence in depth.
//
// session_id is a telemetry-only random ID generated by this module.
// It must NEVER be derived from log files, file names, or the Claude/Codex
// session_id. It is stored in sessionStorage (tab lifetime only) so it cannot
// accumulate into a permanent user identity.
//
// 4 event types (from codex-r4-meta-privacy.md):
//   auto_detect_started    — FSA flow begins
//   auto_detect_completed  — upload accepted
//   auto_detect_failed     — user abandoned or error occurred
//   survey_responded       — post-upload survey answer

// ── Types ─────────────────────────────────────────────────────────────────────

export type DurationBucket = "0-1m" | "1-3m" | "3-5m" | "5-10m" | "10-20m" | "20m+";

export type AutoDetectResult = "summary_generated" | "upload_accepted";

export type FailureCode =
  | "fsa_unavailable"
  | "folder_rejected"
  | "permission_denied"
  | "no_supported_logs"
  | "parse_failed"
  | "summary_validation_failed"
  | "upload_failed"
  | "unknown";

export type FailureStage =
  | "capability_check"
  | "folder_picker"
  | "scan"
  | "parse"
  | "validate"
  | "upload";

export type HardestStep =
  | "terminal_setup"
  | "folder_selection"
  | "browser_permission"
  | "upload"
  | "understanding_results"
  | "other_predefined";

// Only YYYY-MM-DD (not a full timestamp — no time information that could
// correlate to a specific log entry).
type WeekKey = string;

interface BaseEvent {
  readonly event: string;
  readonly schemaVersion: 1;
  readonly weekKey: WeekKey;
  readonly session_id: string;
}

export interface AutoDetectStartedEvent extends BaseEvent {
  readonly event: "auto_detect_started";
  readonly fsaSupported: boolean;
}

export interface AutoDetectCompletedEvent extends BaseEvent {
  readonly event: "auto_detect_completed";
  readonly durationBucket: DurationBucket;
  readonly result: AutoDetectResult;
}

export interface AutoDetectFailedEvent extends BaseEvent {
  readonly event: "auto_detect_failed";
  readonly durationBucket: DurationBucket;
  readonly failureCode: FailureCode;
  readonly failureStage: FailureStage;
}

export interface SurveyRespondedEvent extends BaseEvent {
  readonly event: "survey_responded";
  readonly hardestStep: HardestStep;
  readonly setupTimeBucket: DurationBucket;
}

export type TelemetryEvent =
  | AutoDetectStartedEvent
  | AutoDetectCompletedEvent
  | AutoDetectFailedEvent
  | SurveyRespondedEvent;

// ── Session ID ────────────────────────────────────────────────────────────────

const SESSION_KEY = "cl_telemetry_session_id";

// Returns a telemetry-only session ID (128-bit random hex) scoped to the
// current browser tab (sessionStorage). A new tab gets a new ID.
function getOrCreateSessionId(): string {
  if (typeof sessionStorage === "undefined") return generateSessionId();
  let id = sessionStorage.getItem(SESSION_KEY);
  if (!id || !/^[a-f0-9]{32}$/.test(id)) {
    id = generateSessionId();
    sessionStorage.setItem(SESSION_KEY, id);
  }
  return id;
}

function generateSessionId(): string {
  const buf = new Uint8Array(16);
  if (typeof crypto !== "undefined" && crypto.getRandomValues) {
    crypto.getRandomValues(buf);
  } else {
    // Node.js fallback (tests)
    for (let i = 0; i < 16; i++) buf[i] = Math.floor(Math.random() * 256);
  }
  return Array.from(buf).map((b) => b.toString(16).padStart(2, "0")).join("");
}

// ── Week key ─────────────────────────────────────────────────────────────────

function todayWeekKey(): string {
  const now = new Date();
  const y = now.getUTCFullYear().toString().padStart(4, "0");
  const m = (now.getUTCMonth() + 1).toString().padStart(2, "0");
  const d = now.getUTCDate().toString().padStart(2, "0");
  return `${y}-${m}-${d}`;
}

